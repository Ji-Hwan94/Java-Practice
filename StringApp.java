
public class StringApp {

	public static void main(String[] args) {
		
		
		System.out.println("Hello World");
		System.out.println('H'); //character 하나의 글자
		
		System.out.println("Hello" 
				+ "World"); //줄바꿈 안됨
		//new line
		System.out.println("Hello \nWorld");
		
		
		//escape
		System.out.println("Hello \"World\"");
		
	}

}
